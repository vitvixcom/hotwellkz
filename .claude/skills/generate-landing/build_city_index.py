for p in pub:
    city=p.get("city"); slug=p.get("slug","")
    if city=="Казахстан" or city not in COORDS: continue
    cat=cat_of(slug)
    if not cat: continue
    c=cities.setdefault(city,{"name":city,"lat":COORDS[city][0],"lon":COORDS[city][1],"pages":{}})
    c["pages"].setdefault(cat, slug+".html")   # первый лендинг категории

